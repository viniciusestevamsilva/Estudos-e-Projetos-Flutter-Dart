void main() {
  // Lista do tipo String
  List<String> pantera69 = [
    'Kelly Garrett',
    'Jill Munroe',
    'Sabrina Duncan'];
  
  for (String personagem in pantera69) {
    print(personagem);
  }
}

/**
 * Neste exemplo, a lista de strings pantera69 é
 * inicializada com 3 elementos. Em seguida,
 * o "for-in" é usado para iterar sobre cada
 * elemento da lista a imprimir na saída padrão.
 * A variável personagem é declarada como do
 * tipo String e receberá cada elemento da
 * lista em cada iteração
 */